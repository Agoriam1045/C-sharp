public class Person
{
    public string Name;
    public string Address;
    public int Age;
    public char Sex;
    public string Eye_color;

    public Person (string Name, string Address, int Age, char Sex, string Eye_color )
    {
        this.Name = Name;
        this.Address = Address;
        this.Age = Age;
        this.Sex = Sex;
        this.Eye_color = Eye_color;
    }
}